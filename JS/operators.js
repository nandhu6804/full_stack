//Assignment Operator
a = 10
console.log("a=", a);

//Unary operator
a++;
console.log("\n", a);
--a;
console.log(a);
console.log("a=", a)

//arithmetic operators
a = a + 10;
console.log("\n", a);
a = a - 10;
console.log(a);
console.log("a=", a)

//Relational operators
b=20
c=b<a;
console.log("\n",c);
console.log(b>a);
console.log("a=",a," b=",b)

//Logical Operators
b = b + 5
console.log("\n", b && a)
console.log(b || a)
console.log("a=", a, " b=", b)

//Bitwise operators
console.log("\n", b | a)
console.log(b & a)
console.log(b ^ a)
console.log(b << a)
console.log(b >> a)
console.log("a=", a, " b=", b)

//Ternary Operators
console.log("\n", a < b ? a : b)
console.log("a=", a)
